"""Exponente"""
print(2 ** 3)
print(2 ** 3.)
print(2. ** 3)
print(2. ** 3.)
print( 2 ** 2 ** 3)
print("\n")
"""Multiplicacion"""
print(2 * 3)
print(2 * 3.)
print(2. * 3)
print(2. * 3.)
print("\n")
"""Division"""
print(6 / 3)
print(6 / 3.)
print(6. / 3)
print(6. / 3.)
print("\n")
"""Notacion Cientifica"""
print(1e-4)
print(2e3)
print("\n")
"""Division de enteros"""
print(6 // 3)
print(6 // 3.)
print(6. // 3)
print(6. // 3.)
print(6 // 4)
print(6. // 4)
print(-6 // 4)
print(6. // -4)
print("\n")
"""Modulo"""
print(14 % 4)
print(12 % 4.5)
print(9 % 6 % 2)
print("\n")
"""Suma y Resta"""
print(-4 + 4)
print(-4. + 8)
print(-1.1)
print(+2)
print("\n")
"""Jerarquia de Operadores"""
print(10 + 2 * (30 / 90))
print(2 * 3 % 5)
print((5 * ((25 % 13) + 100) / (2 * 13)) // 2)
